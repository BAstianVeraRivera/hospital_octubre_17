public class Paciente {
	private int edad;
	private String nombre;
	private String sexo;
	private int rut;
	private String jugadordeLOL;

	public int getEdad() {
		return this.edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	public String getNombre() {
		return this.nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getSexo() {
		return this.sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}

	public int getRut() {
		return this.rut;
	}

	public void setRut(int rut) {
		this.rut = rut;
	}

	public String getJugadordeLOL() {
		return this.jugadordeLOL;
	}

	public void setJugadordeLOL(String jugadordeLOL) {
		this.jugadordeLOL = jugadordeLOL;
	}
}